package com.kosmo.vo;

public class OfferVO {

	
	private int oseq;
	private String otitle;
	private String obody;
	private int oregdate;
	private int oview;
	private String odel;
	
	
	public int getOseq() {
		return oseq;
	}
	public void setOseq(int oseq) {
		this.oseq = oseq;
	}
	public String getOtitle() {
		return otitle;
	}
	public void setOtitle(String otitle) {
		this.otitle = otitle;
	}
	public String getObody() {
		return obody;
	}
	public void setObody(String obody) {
		this.obody = obody;
	}
	public int getOregdate() {
		return oregdate;
	}
	public void setOregdate(int oregdate) {
		this.oregdate = oregdate;
	}
	public int getOview() {
		return oview;
	}
	public void setOview(int oview) {
		this.oview = oview;
	}
	public String getOdel() {
		return odel;
	}
	public void setOdel(String odel) {
		this.odel = odel;
	}
	
	
}
